package dev.cammiescorner.arcanuscontinuum.common.spell_components.effects.movement;

import dev.cammiescorner.arcanuscontinuum.ArcanusConfig;
import dev.cammiescorner.arcanuscontinuum.api.spells.SpellEffect;
import dev.cammiescorner.arcanuscontinuum.api.spells.SpellType;
import dev.cammiescorner.arcanuscontinuum.api.spells.Weight;
import dev.cammiescorner.arcanuscontinuum.common.registry.ArcanusSpellComponents;
import net.minecraft.entity.Entity;
import net.minecraft.entity.LivingEntity;
import net.minecraft.item.ItemStack;
import net.minecraft.util.hit.EntityHitResult;
import net.minecraft.util.hit.HitResult;
import net.minecraft.util.math.Vec3d;
import net.minecraft.world.World;
import org.jetbrains.annotations.Nullable;

import java.util.List;

public class ExchangeSpellEffect extends SpellEffect {
	public ExchangeSpellEffect(boolean isEnabled, SpellType type, Weight weight, double manaCost, int coolDown, int minLevel) {
		super(isEnabled, type, weight, manaCost, coolDown, minLevel);
	}

	@Override
	public void effect(@Nullable LivingEntity caster, @Nullable Entity sourceEntity, World world, HitResult target, List<SpellEffect> effects, ItemStack stack, double potency) {
		if(target.getType() == HitResult.Type.ENTITY) {
			EntityHitResult entityHit = (EntityHitResult) target;

			if(entityHit.getEntity() instanceof LivingEntity livingEntity && caster != null && caster.distanceTo(livingEntity) <= ArcanusConfig.MovementEffects.ExchangeEffectProperties.baseTeleportDistance * effects.stream().filter(ArcanusSpellComponents.EXCHANGE::is).count() * potency) {
				Vec3d casterPos = caster.getPos();
				Vec3d targetPos = livingEntity.getPos();

				caster.teleport(targetPos.getX(), targetPos.getY(), targetPos.getZ(), true);
				livingEntity.teleport(casterPos.getX(), casterPos.getY(), casterPos.getZ(), true);
			}
		}
	}
}
